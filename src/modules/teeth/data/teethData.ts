// Función helper para obtener diente por número
export function getToothByNumber(numero: number): ToothData | undefined {
  return TEETH_DATA.find((t) => t.numero === numero)
}

// Función helper para obtener dientes por cuadrante
export function getTeethByQuadrant(cuadrante: number): ToothData[] {
  return TEETH_DATA.filter((t) => t.cuadrante === cuadrante)
}

// Función helper para obtener dientes por elemento
export function getTeethByElement(elemento: string): ToothData[] {
  return TEETH_DATA.filter((t) => t.elemento === elemento)
}
